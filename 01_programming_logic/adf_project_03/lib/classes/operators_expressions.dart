void main() {
  /// Operators:
  // (>) - Verify left is bigger than right (2 > 1)
  // (>=) - Verify left is bigger or equal than right (2 >=1) (2 >= 2)
  // (<) - Verify left is less than right (1 < 2)
  // (<=) - Verify left is less or equal than right (1 <= 2) (2 <= 2)
  // (==) - Verify left is equal than right (1 == 1)
  // (!=) - Verify left is different than right (1 != 2)

  /// Relational Expressions:
  print(1 < 2);
  print(1 != 5);
}